import { Router } from 'express';
import { UserController } from '../controllers';
import AuthMiddleware from '../middlewares/auth.middleware';

const router: Router = Router();
const userController = new UserController();

/**
 * @swagger
 * tags:
 *   name: Users
 *   description: User profile and management
 */

/**
 * @swagger
 * /users/profile:
 *   get:
 *     summary: Get current authenticated user profile
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: User profile retrieved successfully
 *       401:
 *         description: Unauthorized
 */
// region GET - /users/profile
router.get('/profile', AuthMiddleware.verifyUser, userController.getProfile);

/**
 * @swagger
 * /users/profile:
 *   put:
 *     summary: Update current authenticated user profile
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               fullName:
 *                 type: string
 *               avatarConfig:
 *                 type: object
 *     responses:
 *       200:
 *         description: Profile updated successfully
 *       401:
 *         description: Unauthorized
 */
// region PUT - /users/profile
router.put('/profile', AuthMiddleware.verifyUser, userController.updateProfile);

/**
 * @swagger
 * /users/fcm-token:
 *   post:
 *     summary: Update user's FCM token for notifications
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - fcmToken
 *             properties:
 *               fcmToken:
 *                 type: string
 *                 description: The FCM token generated by the mobile app
 *     responses:
 *       200:
 *         description: FCM token updated successfully
 *       400:
 *         description: FCM token is required
 *       401:
 *         description: Unauthorized
 */
// region POST - /users/fcm-token
router.post('/fcm-token', AuthMiddleware.verifyUser, userController.updateFcmToken);

/**
 * @swagger
 * /users/notifications:
 *   get:
 *     summary: Get all notifications for current authenticated user
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: Notifications retrieved successfully
 *       401:
 *         description: Unauthorized
 */
// region GET - /users/notifications
router.get('/notifications', AuthMiddleware.verifyUser, userController.getNotifications);

/**
 * @swagger
 * /users/change-password:
 *   put:
 *     summary: Change user password
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - oldPassword
 *               - newPassword
 *             properties:
 *               oldPassword:
 *                 type: string
 *               newPassword:
 *                 type: string
 *     responses:
 *       200:
 *         description: Password changed successfully
 *       400:
 *         description: Invalid input or current password incorrect
 *       401:
 *         description: Unauthorized
 */
// region PUT - /users/change-password
router.put('/change-password', AuthMiddleware.verifyUser, userController.changePassword);

export default router;
